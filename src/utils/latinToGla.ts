export function latinToGla(text: string): string {
    return text
        .replace(/[å|a]/g, 'Ⰰ')
        .replace(/b/g, 'Ⰱ')
        .replace(/c/g, 'Ⱌ')
        .replace(/č/g, 'Ⱍ')
        .replace(/dj/g, 'ⰄⰬ')
        .replace(/dž/g, 'Ⰼ')
        .replace(/d/g, 'Ⰴ')
        .replace(/[e|ę]/g, 'Ⰵ')
        .replace(/ě/g, 'Ⱑ')
        .replace(/f/g, 'Ⱇ')
        .replace(/g/g, 'Ⰳ')
        .replace(/h/g, 'Ⱈ')
        .replace(/i/g, 'ⰻ')
        .replace(/ju/g, 'Ⱓ')
        .replace(/j/g, 'Ⱜ')
        .replace(/k/g, 'Ⰽ')
        .replace(/lj/g, 'ⰎⰬ')
        .replace(/l/g, 'Ⰾ')
        .replace(/m/g, 'Ⰿ')
        .replace(/nj/g, 'ⰐⰬ')
        .replace(/n/g, 'Ⱀ')
        .replace(/o/g, 'Ⱁ')
        .replace(/p/g, 'Ⱂ')
        .replace(/q/g, 'Ⰽ')
        .replace(/r/g, 'Ⱃ')
        .replace(/s/g, 'Ⱄ')
        .replace(/šč/g, 'Ⱋ')
        .replace(/š/g, 'Ⱎ')
        .replace(/tj/g, 'ⰕⰬ')
        .replace(/t/g, 'Ⱅ')
        .replace(/u/g, 'Ⱆ')
        .replace(/v/g, 'Ⰲ')
        .replace(/x/g, 'ⰍⰔ')
        .replace(/y/g, 'Ⱐ')
        .replace(/z/g, 'Ⰷ')
        .replace(/ž/g, 'Ⰶ')
    ;
}
